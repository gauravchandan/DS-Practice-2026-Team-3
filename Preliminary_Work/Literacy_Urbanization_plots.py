uploaded = files.upload()  # upload both CSVs together

df_literacy = pd.read_csv('State-wise_literacy_rates_and_increase-in_literacy_rates_as_per_Census_during_2001_and_2011.csv')
df_ru = pd.read_csv('State_UT-wise_Rural_and_Urban_Population_as_Per_Census_during_2001_and_2011.csv')

