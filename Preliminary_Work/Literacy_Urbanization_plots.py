# ── Clean df_literacy ──────────────────────────────────────────────
# Drop India total row
df_literacy = df_literacy[df_literacy['S. No.'] != 'INDIA'].copy()

# Drop S.No column
df_literacy.drop(columns=['S. No.'], inplace=True)

# Rename columns
df_literacy.columns = [
    'State',
    'Lit_Total_2001', 'Lit_Total_2011',
    'Lit_Male_2001', 'Lit_Male_2011',
    'Lit_Female_2001', 'Lit_Female_2011',
    'Inc_Total', 'Inc_Male', 'Inc_Female'
]

df_literacy['State'] = df_literacy['State'].str.strip()
df_literacy = df_literacy.reset_index(drop=True)

print("Literacy cleaned shape:", df_literacy.shape)
print(df_literacy.head(3))

# ── Clean df_ru ────────────────────────────────────────────────────
# Drop Note column and S.No
df_ru.drop(columns=['S. No.', 'Note of State/ UT'], inplace=True)

# Remove All India total row
df_ru = df_ru[~df_ru['State/ UT'].str.contains('Total', na=False)].copy()

# Rename columns
df_ru.columns = [
    'State',
    'Rural_Pop_2001', 'Urban_Pop_2001', 'Total_Pop_2001', 'Rural_Pct_2001',
    'Rural_Pop_2011', 'Urban_Pop_2011', 'Total_Pop_2011', 'Rural_Pct_2011'
]

df_ru['Urban_Pct_2011'] = (100 - df_ru['Rural_Pct_2011']).round(2)
df_ru['Urban_Pct_2001'] = (100 - df_ru['Rural_Pct_2001']).round(2)
df_ru['State'] = df_ru['State'].str.strip()
df_ru = df_ru.reset_index(drop=True)

print("\nRural Urban cleaned shape:", df_ru.shape)
print(df_ru.head(3))

# Check what's in ru but not in literacy
print("In ru but not in literacy:",
      set(df_ru['State']) - set(df_literacy['State']))

print("In literacy but not in ru:",
      set(df_literacy['State']) - set(df_ru['State']))

# Fix state names in df_ru to match df_literacy
ru_fixes = {
    'A & N Island': 'Andaman & Nicobar Islands',
    'Uttarakhand': 'Uttrakhand',  # match literacy spelling
    'Telangana': 'Telangana'      # not in literacy, will be NaN after merge — expected
}

df_ru['State'] = df_ru['State'].replace(ru_fixes)

# Verify
print("Remaining mismatches:", set(df_ru['State']) - set(df_literacy['State']))

df_state = pd.merge(df_literacy, df_ru, on='State', how='outer')

print("df_state shape:", df_state.shape)
print("Missing values:\n", df_state.isnull().sum()[df_state.isnull().sum() > 0])
print(df_state.head(3))

